export function detectLang(): Lang {
  try {
    const saved = localStorage.getItem("valmaker_lang");
    if (saved === "en" || saved === "zh") return saved;
  } catch { /* ignore */ }
  const nav = navigator.language || "";
  return nav.toLowerCase().startsWith("en") ? "en" : "zh";
}
